#input
bilangan_pertama = int(input("Masukkan bilangan pertama: "))

#input2
bilangan_kedua = int(input("Masukkan bilangan kedua: "))

#penjumlahan
print(f"Hasil penjumlahan: {bilangan_pertama + bilangan_kedua}")

#pengurangan
print(f"Hasil pengurangan: {bilangan_pertama - bilangan_kedua}")

#modulus
print(f"Hasil modulus: {bilangan_pertama % bilangan_kedua}")